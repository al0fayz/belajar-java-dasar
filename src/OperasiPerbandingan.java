public class OperasiPerbandingan {
    public static void main(String[] args) {
        //contoh operasi perbandingan
        /**
         * ==
         * >=
         * <=
         * !=
         * >
         * <
         */
        int a = 90;
        int b = 100;

        System.out.println(a == b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a != b);
        System.out.println(a < b);
        System.out.println(a > b);
    }
}
